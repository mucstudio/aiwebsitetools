export default function EmojiPicker({ value, onChange, onClose }: EmojiPickerProps) {
  const [selectedCategory, setSelectedCategory] = useState<string>(Object.keys(EMOJI_CATEGORIES)[0])

  const filteredEmojis = EMOJI_CATEGORIES[selectedCategory as keyof typeof EMOJI_CATEGORIES] || []

  return (
    <div className="absolute z-50 mt-2 left-0 right-0 bg-white border border-gray-300 rounded-lg shadow-xl">
      <div className="p-2 border-b border-gray-200">
        <div className="flex items-center justify-between">
          <h3 className="font-semibold text-gray-900">Select Icon</h3>
          {onClose && (
            <button
              onClick={onClose}
              className="p-1 hover:bg-gray-100 rounded transition-colors"
              type="button"
            >
              <X className="w-4 h-4" />
            </button>
          )}
        </div>
      </div>

      <div className="flex overflow-x-auto border-b border-gray-200 bg-gray-50">
        {Object.keys(EMOJI_CATEGORIES).map((category) => (
          <button
            key={category}
            type="button"
            onClick={() => setSelectedCategory(category)}
            className={`px-3 py-2 text-xs font-medium whitespace-nowrap transition-colors ${
              selectedCategory === category
                ? 'text-primary-600 border-b-2 border-primary-600 bg-white'
                : 'text-gray-600 hover:text-gray-900'
            }`}
          >
            {category}
          </button>
        ))}
      </div>

      <div className="p-2 max-h-64 overflow-y-auto">
        <div className="grid gap-1" style={{ gridTemplateColumns: 'repeat(16, minmax(0, 1fr))' }}>
          {filteredEmojis.map((emoji, index) => (
            <button
              key={index}
              type="button"
              onClick={() => {
                onChange(emoji)
                onClose?.()
              }}
              className={`text-lg p-1 rounded hover:bg-gray-100 transition-colors flex items-center justify-center ${
                value === emoji ? 'bg-primary-50 ring-1 ring-primary-500' : ''
              }`}
              title={emoji}
            >
              {emoji}
            </button>
          ))}
        </div>

        {filteredEmojis.length === 0 && (
          <div className="text-center py-8 text-gray-500">
            No emojis found
          </div>
        )}
      </div>

      <div className="p-3 border-t border-gray-200 bg-gray-50">
        <div className="flex items-center justify-between">
          <span className="text-sm text-gray-600">Selected:</span>
          <span className="text-3xl">{value || '🔧'}</span>
        </div>
      </div>
    </div>
  )
}
