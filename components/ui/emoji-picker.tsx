interface EmojiPickerProps {
  value: string
  onChange: (emoji: string) => void
}

export function EmojiPicker({ value, onChange }: EmojiPickerProps) {
  const [open, setOpen] = useState(false)
  const [search, setSearch] = useState("")

  const filteredEmojis = search
    ? Object.entries(EMOJI_CATEGORIES).reduce((acc, [category, emojis]) => {
        const filtered = emojis.filter(emoji => emoji.includes(search))
        if (filtered.length > 0) {
          acc[category] = filtered
        }
        return acc
      }, {} as Record<string, string[]>)
    : EMOJI_CATEGORIES

  const handleSelect = (emoji: string) => {
    onChange(emoji)
    setOpen(false)
  }

  return (
    <Popover open={open} onOpenChange={setOpen}>
      <PopoverTrigger asChild>
        <Button variant="outline" className="w-full justify-start text-left font-normal">
          {value ? (
            <span className="text-2xl">{value}</span>
          ) : (
            <span className="text-muted-foreground">é€‰æ‹©è¡¨æƒ…</span>
          )}
        </Button>
      </PopoverTrigger>
      <PopoverContent className="w-[400px] p-0" align="start">
        <div className="p-3 border-b">
          <Input
            placeholder="æœç´¢è¡¨æƒ…..."
            value={search}
            onChange={(e) => setSearch(e.target.value)}
            className="h-9"
          />
        </div>
        <Tabs defaultValue={Object.keys(EMOJI_CATEGORIES)[0]} className="w-full">
          <TabsList className="w-full justify-start rounded-none border-b bg-transparent p-0">
            <ScrollArea className="w-full">
              <div className="flex">
                {Object.keys(filteredEmojis).map((category) => (
                  <TabsTrigger
                    key={category}
                    value={category}
                    className="rounded-none border-b-2 border-transparent data-[state=active]:border-primary"
                  >
                    {category}
                  </TabsTrigger>
                ))}
              </div>
            </ScrollArea>
          </TabsList>
          <ScrollArea className="h-[300px]">
            {Object.entries(filteredEmojis).map(([category, emojis]) => (
              <TabsContent key={category} value={category} className="p-3 m-0">
                <div className="grid grid-cols-8 gap-2">
                  {emojis.map((emoji) => (
                    <button
                      key={emoji}
                      type="button"
                      onClick={() => handleSelect(emoji)}
                      className="text-2xl hover:bg-muted rounded p-2 transition-colors"
                    >
                      {emoji}
                    </button>
                  ))}
                </div>
              </TabsContent>
            ))}
          </ScrollArea>
        </Tabs>
      </PopoverContent>
    </Popover>
  )
}
